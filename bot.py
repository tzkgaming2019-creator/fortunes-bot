async def button_handler(update: Update, context: ContextTypes.DEFAULT_TYPE):
    query = update.callback_query
    user_name = query.from_user.first_name
    user_mention = f"[{user_name}](tg://user?id={query.from_user.id})"
    await query.answer()

    if query.data == "fortune":
        res = random.choice(fortunes)
        await query.message.reply_text(f" {user_mention} ✨\n\n{res}", parse_mode="Markdown")
    
    elif query.data == "love":
        res = random.choice(love_fortunes)
        love_msg = (
            f"💖 {user_mention} ✨\n"
            "◈━━━━━━━━━━━━━━━━━━◈\n"
            f" {res}\n"
            "◈━━━━━━━━━━━━━━━━━━◈"
        )
        await query.message.reply_text(love_msg, parse_mode="Markdown")
        
    elif query.data == "help":
        help_guide = (
            "🇲🇲 SHWE MM ဗေဒင်လမ်းညွှန်\n\n"
            "🔮 ဗေဒင်မေးရန်: /readmyfortune\n"
            "💖 အချစ်ရေးမေးရန်: /readmylove\n"
            "🎵 Musicbot များ\n"
            "➤ @SHWEMM_MUSIC1bot\n"
            "➤ @SHWEMM_MUSIC2bot\n"
            "➤ @SHWEMM_MUSIC3bot\n"
            "➤ @SHWEMM_MUSIC4bot"
        )
        await query.message.reply_text(help_guide, parse_mode="Markdown")

def main():
    if not TOKEN: 
        return
    app = ApplicationBuilder().token(TOKEN).build()
    
    # Handlers
    app.add_handler(CommandHandler("start", start))
    app.add_handler(CommandHandler("readmyfortune", readmyfortune))
    app.add_handler(CommandHandler("readmylove", readmylove))
    app.add_handler(CallbackQueryHandler(button_handler))
    
    print("✅ Bot is running perfectly...")
    app.run_polling()

if __name__ == "__main__":
    main()
